static void matmul(double *w, const Mesh *mesh, const double *p)
{
  int    *len = mesh->len;
  int    *col = mesh->col;
  double *dat = mesh->dat;
  double *n;
  double *o;

  register double x0, x1, x2;
  register double y0, y1, y2;
  register double m0, m1, m2;
  register double d0, d1, d2, d3, d4, d5, d6, d7, d8;

  const int nn = mesh->nn;
  int c;
  int i, j, l;

  for (i = 0; i < nn; ++i) {
    /* Get x components and modification for diagonal block */
    l = *len++;
    c = *col++;
    o = w + c;

    x0 = p[c+0];
    x1 = p[c+1];
    x2 = p[c+2];

    m0 = dat[0]*x0 + dat[1]*x1 + dat[2]*x2;
    m1 = dat[1]*x0 + dat[3]*x1 + dat[4]*x2;
    m2 = dat[2]*x0 + dat[4]*x1 + dat[5]*x2;

    dat += 6;

    /* Calculate the off diagonal blocks */
    for (j = 1; j < l; ++j) {
      c = *col++;
      n = w + c;
     
      y0 = p[c+0];
      y1 = p[c+1];
      y2 = p[c+2];

      n[0] += (d0 = dat[0])*x0 + (d3 = dat[3])*x1 + (d6 = dat[6])*x2;
      n[1] += (d1 = dat[1])*x0 + (d4 = dat[4])*x1 + (d7 = dat[7])*x2;
      n[2] += (d2 = dat[2])*x0 + (d5 = dat[5])*x1 + (d8 = dat[8])*x2;

      m0 += d0*y0 + d1*y1 + d2*y2;
      m1 += d3*y0 + d4*y1 + d5*y2;
      m2 += d6*y0 + d7*y1 + d8*y2;

      dat += 9;
    }

    /* Add modifiation */
    o[0] += m0;
    o[1] += m1;
    o[2] += m2;
  }
  return;
}

